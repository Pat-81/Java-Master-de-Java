package Bases;


public class CaracteresSpeciaux {
    public static void main(String args[]) {
        var nom = "Karla";

        System.out.println("Nouvelle Ligne : \n" + nom);
        System.out.println("Tabulation : \t" + nom);
        System.out.println("Retour arrière : \b" + nom);
        System.out.println("Retour arrière : \b\b" + nom);
        System.out.println("Guilemets simples : \'" + nom + "\'");
        System.out.println("Guilemets simples : \"" + nom + "\"");
    }
}
